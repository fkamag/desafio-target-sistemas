public class DesafioProgressao {

  public static void progressao() {
    System.out.println("Desafio de Progressão");
    System.out.println("a) 1, 3, 5, 7, 9  -  o próximo é sempre 2 a mais que o anterior");
    System.out.println("b) 2, 4, 8, 16, 32, 64, 128  -  o próximo é sempre o dobro do anterior");
    System.out.println("c) 0, 1, 4, 9, 16, 25, 36, 49  -  sequência de quadrados a partir de 0");
    System.out.println("d) 4, 16, 36, 64, 100  -  sequência de quadrados dos números pares");
    System.out.println("e) 1, 1, 2, 3, 5, 8, 13  -  sequência de Fibonacci iniciando em 1 e 1");
    System.out.println("f) 2, 10, 12, 16, 17, 18, 19, 200  -  sequência de números começados com a letra d");
  }

}
